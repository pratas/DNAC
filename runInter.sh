#!/bin/bash
#==============================================================================
GET_GOOSE=1;
GET_GULL=1;
#==============================================================================
GET_DNA=1;
#==============================================================================
RUN_GULL=1;
#==============================================================================
###############################################################################
#==============================================================================
# GET GOOSE
if [[ "$GET_GOOSE" -eq "1" ]]; then
  rm -fr goose/ goose-*
  git clone https://github.com/pratas/goose.git
  cd goose/src/
  make
  cd ../../
  cp goose/src/goose-* .
  cp goose/scripts/Get*.sh .
fi
#==============================================================================
# GET GECO
if [[ "$GET_GULL" -eq "1" ]]; then
  rm -fr GULL/
  git clone https://github.com/pratas/GULL.git
  cd GULL/src/
  cmake .
  make
  cp GULL-map ../../
  cp GULL-visual ../../
  cd ../../
fi
#==============================================================================
###############################################################################
#==============================================================================
if [[ "$GET_DNA" -eq "1" ]]; then
  wget http://sweet.ua.pt/pratas/datasets/DNACorpus.zip
  unzip DNACorpus.zip
  mv DNACorpus/* .
fi
#==============================================================================
###############################################################################
#==============================================================================
if [[ "$RUN_GULL" -eq "1" ]]; then
  (./GULL-map -v -m 20:500:1:5/20 -m 14:100:1:1/1 -m 13:50:0:0/0 -m 11:10:0:0/0 -m 6:1:0:0/0 -m 3:1:0:0/0 -c 100 -g 0.9 -x matrix.csv OrSa:HoSa:GaGa:DaRe:DrMe:EnIn:ScPo:PlFa:EsCo:HePy:AeCa:HaHi:YeMi:BuEb:AgPh ) &> REPORT_MAP;
printf "OrSa\nHoSa\nGaGa:DaRe\nDrMe\nEnIn\nScPo\nPlFa\nEsCo\nHePy\nAeCa\nHaHi\nYeMi\nBuEb\nAgPh" > labels;
  ./GULL-visual -w 100 -a 20 -l labels matrix.csv
fi
#==============================================================================
###############################################################################
#==============================================================================
